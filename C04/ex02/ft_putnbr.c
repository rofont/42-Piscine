#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	nb1;

	if (nb < 0)
	{
		if (nb == -2147483648)
			write(1, "-2147483648", 11);
		else
		{
			write(1, "-", 1);
			nb = nb * -1;
		}
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		nb = nb % 10;
	}
	if (nb >= 0)
	{
		nb1 = nb + 48;
		write (1, &nb1, 1);
	}
}
